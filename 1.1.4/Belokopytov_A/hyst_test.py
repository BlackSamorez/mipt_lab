import matplotlib.pyplot as plt
import numpy as np

# Установим стиль для лучшей визуализации
plt.style.use('ggplot')

# 1. Создание данных
np.random.seed(42)
# Данные 1: Узкий диапазон (для первой, основной оси X)
data_primary = np.random.normal(loc=5, scale=1, size=500)
# Данные 2: Широкий диапазон (для второй, вспомогательной оси X)
data_secondary = np.random.normal(loc=500, scale=100, size=500) 

# 2. Создание основного графика
fig, ax1 = plt.subplots(figsize=(10, 6))

# --- Гистограмма 1 (Основная ось X) ---
ax1.hist(data_primary, bins=20, color='skyblue', alpha=0.6, label='Узкий диапазон данных', density=True)
ax1.set_xlabel("Основной масштаб X (0 - 10)", color='darkblue')
ax1.tick_params(axis='x', colors='darkblue')
ax1.set_ylabel("Плотность (частота)", color='black')
ax1.set_xlim(0, 10) # Устанавливаем узкий масштаб X для ax1

# 3. Создание вторичной оси X (ax2)
# twinx() используется для Y, а twinx().xaxis.tick_top() для X
# Самый чистый способ - создать основной график и затем создать его "близнеца"
ax2 = ax1.twiny()

# --- Гистограмма 2 (Вторичная ось X) ---
# Рисуем гистограмму на вторичных осях (ax2). Важно: ax2 должен иметь свою ось X.
# Мы будем использовать параметр bottom=1.005, чтобы немного приподнять вторую ось X,
# иначе она будет точно поверх первой, и подписи сольются.
# Внимание: для гистограмм с разными масштабами X нужно убедиться, что ось Y
# также масштабируется корректно, либо использовать density=True.

# Нарисуем ее на ax2, чтобы она использовала ax2.set_xlim()
# Мы не можем просто нарисовать ax2.hist(data_secondary), так как это будет использовать
# шкалу Y от ax1, но X от ax2. Однако гистограмма будет выглядеть странно,
# потому что ее "колонки" будут отсчитываться от нижней оси X.
#
# Чтобы избежать визуального конфликта, часто лучше использовать другую визуализацию
# для второй оси, например, линию или точки, но для гистограммы мы можем
# попробовать скомпенсировать ее расположение:

# Перерисуем Гистограмму 2, используя ax2, но сделаем ее прозрачной,
# чтобы не мешать основной визуализации, и добавим метки.
ax2.hist(data_secondary, bins=20, color='salmon', alpha=0.3, label='Широкий диапазон данных', density=True)
ax2.set_xlabel("Вторичный масштаб X (0 - 1000)", color='darkred', labelpad=15) # Сдвигаем метку вверх
ax2.tick_params(axis='x', colors='darkred')
ax2.set_xlim(0, 1000) # Устанавливаем широкий масштаб X для ax2

# Размещение меток второй оси X вверху
ax2.xaxis.set_ticks_position('top')
ax2.xaxis.set_label_position('top')

# Общий заголовок
fig.suptitle('Две гистограммы на одном графике с независимыми осями X')

# Легенда
# Объединяем легенды с двух осей
lines1, labels1 = ax1.get_legend_handles_labels()
lines2, labels2 = ax2.get_legend_handles_labels()
ax1.legend(lines1 + lines2, labels1 + labels2, loc='upper right')

plt.tight_layout(rect=[0, 0, 1, 0.95]) # Корректировка для заголовка
plt.show()
